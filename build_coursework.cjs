// ---------- helpers ----------
const FONT = 'Times New Roman';
const FONT_CODE = 'Consolas';
const SZ = 28;        // 14pt main text
const SZ_TITLE = 32;  // 16pt
const SZ_H = 28;      // 14pt
const SZ_CODE = 22;   // 11pt
const LINE = 360;     // 1.5 spacing in 240ths
const FIRST_LINE_INDENT = 720; // 1.27cm

function tr(text, opts = {}) {
  return new TextRun({
    text,
    font: opts.font || FONT,
    size: opts.size ?? SZ,
    bold: opts.bold ?? false,
    italic: opts.italic ?? false,
    color: opts.color ?? '000000',
  });
}

function p(text, opts = {}) {
  const runs = Array.isArray(text) ? text : [tr(text, opts)];
  return new Paragraph({
    children: runs,
    alignment: opts.alignment ?? AlignmentType.JUSTIFIED,
    spacing: { line: LINE, after: opts.after ?? 0, before: opts.before ?? 0 },
    indent: opts.noIndent ? undefined : { firstLine: FIRST_LINE_INDENT },
  });
}

function h1(text) {
  return new Paragraph({
    children: [tr(text, { bold: true, size: SZ_H })],
    alignment: AlignmentType.CENTER,
    spacing: { line: LINE, before: 240, after: 240 },
    pageBreakBefore: true,
  });
}

function h2(text) {
  return new Paragraph({
    children: [tr(text, { bold: true, size: SZ_H })],
    alignment: AlignmentType.LEFT,
    spacing: { line: LINE, before: 240, after: 120 },
    indent: { firstLine: FIRST_LINE_INDENT },
  });
}

function h3(text) {
  return new Paragraph({
    children: [tr(text, { bold: true, size: SZ })],
    alignment: AlignmentType.LEFT,
    spacing: { line: LINE, before: 120, after: 60 },
    indent: { firstLine: FIRST_LINE_INDENT },
  });
}

function par(text) { return p(text); }

function li(text) {
  return new Paragraph({
    children: Array.isArray(text) ? text : [tr(text)],
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: LINE, after: 0 },
    numbering: { reference: 'bullets', level: 0 },
  });
}

function ni(text) {
  return new Paragraph({
    children: Array.isArray(text) ? text : [tr(text)],
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: LINE, after: 0 },
    numbering: { reference: 'numbers', level: 0 },
  });
}

function code(text) {
  return text.split('\n').map(line =>
    new Paragraph({
      children: [new TextRun({ text: line || ' ', font: FONT_CODE, size: SZ_CODE })],
      spacing: { line: 240, after: 0 },
      alignment: AlignmentType.LEFT,
      indent: { left: 360 },
    })
  );
}

function tocLine(text, page) {
  return new Paragraph({
    children: [tr(text), tr('\t' + page)],
    tabStops: [{ type: TabStopType.RIGHT, position: 9000, leader: 'dot' }],
    spacing: { line: LINE, after: 0 },
  });
}

