def rewrite(doc, match, new):
    for idx, p in enumerate(doc.paragraphs):
        if match in p.text:
            runs = p.runs
            if not runs:
                continue
            carrier = next((r for r in runs if r.text.strip()), runs[0])
            for r in runs:
                r.text = ''
            carrier.text = new
            return idx
    return None

for match, new in edits:
    idx = rewrite(doc, match, new)
    if idx is None:
        print(f'NOT FOUND: {match[:50]}')
    else:
        print(f'Rewrote paragraph {idx}: {match[:50]}')

